package com.home.commonScene.net.serverResponse.game.login;
import com.home.commonBase.constlist.generate.ServerMessageType;
import com.home.commonBase.data.role.RoleShowData;
import com.home.commonBase.data.scene.scene.SceneEnterArgData;
import com.home.commonScene.global.SceneC;
import com.home.commonScene.net.serverResponse.game.base.GameToSceneServerResponse;
import com.home.commonScene.net.serverResponse.game.base.PlayerGameToSceneServerResponse;
import com.home.shine.bytes.BytesReadStream;
import com.home.shine.support.DataWriter;
import com.home.shine.support.pool.DataPool;

/** 玩家登录到场景服消息(generated by shine) */
public class PlayerSwitchToSceneServerResponse extends GameToSceneServerResponse
{
	public long playerID;
	
	/** 数据类型ID */
	public static final int dataID=ServerMessageType.PlayerSwitchToScene;
	
	/** 外显数 */
	public RoleShowData showData;
	
	/** 场景进入数据 */
	public SceneEnterArgData enterArg;
	
	public PlayerSwitchToSceneServerResponse()
	{
		_dataID=ServerMessageType.PlayerSwitchToScene;
	}
	
	/** 获取数据类名 */
	@Override
	public String getDataClassName()
	{
		return "PlayerSwitchToSceneServerResponse";
	}
	
	/** 读取字节流(简版) */
	@Override
	protected void toReadBytesSimple(BytesReadStream stream)
	{
		super.toReadBytesSimple(stream);
		
		this.playerID=stream.readLong();
		
		this.showData=(RoleShowData)stream.readDataSimpleNotNull();
		
		this.enterArg=(SceneEnterArgData)stream.readDataSimpleNotNull();
		
	}
	
	/** 转文本输出 */
	@Override
	protected void toWriteDataString(DataWriter writer)
	{
		super.toWriteDataString(writer);
		
		writer.writeTabs();
		writer.sb.append("playerID");
		writer.sb.append(':');
		writer.sb.append(this.playerID);
		
		writer.writeEnter();
		writer.writeTabs();
		writer.sb.append("showData");
		writer.sb.append(':');
		if(this.showData!=null)
		{
			this.showData.writeDataString(writer);
		}
		else
		{
			writer.sb.append("RoleShowData=null");
		}
		
		writer.writeEnter();
		writer.writeTabs();
		writer.sb.append("enterArg");
		writer.sb.append(':');
		if(this.enterArg!=null)
		{
			this.enterArg.writeDataString(writer);
		}
		else
		{
			writer.sb.append("SceneEnterArgData=null");
		}
		
		writer.writeEnter();
	}
	
	/** 回池 */
	@Override
	protected void toRelease(DataPool pool)
	{
		super.toRelease(pool);
		
		this.playerID=0L;
		this.showData=null;
		this.enterArg=null;
	}
	
	/** 执行 */
	@Override
	protected void execute()
	{
		SceneC.main.playerPreSwitch(playerID,showData,enterArg,fromGameID);
	}
	
}
